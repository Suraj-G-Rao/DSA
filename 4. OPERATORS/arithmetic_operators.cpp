#include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"a: ";
    cin>>a;
    cout<<"b: ";
    cin>>b;
    cout<<"Sum of two numbers is: "<<a+b<<endl;
    cout<<"Difference of two numbers is: "<<a-b<<endl;
    cout<<"Product of two numbers is: "<<a*b<<endl;
    cout<<"Division of two numbers is: "<<a/b<<endl;
    cout<<"Remainder of two numbers is: "<<a%b<<endl;
    return 0;
}